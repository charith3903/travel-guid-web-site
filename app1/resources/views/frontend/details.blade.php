          <h2 style="color: #EFDDB6;">About Sri Lanka</h2>
          <p>Sri Lanka, often referred to as the "Pearl of the Indian Ocean," is a tropical paradise that boasts a rich tapestry of landscapes, cultures, and experiences. From its pristine beaches to its lush green hills, Sri Lanka offers a variety of natural beauty that captivates every visitor. The island's scenic beauty is enhanced by its diverse ecosystems, including rainforests, mountains, and arid plains. The cultural heritage of Sri Lanka is equally mesmerizing, with ancient cities, temples, and colonial architecture narrating tales of a bygone era. The country's vibrant traditions, colorful festivals, and warm hospitality further add to its charm, making it a must-visit destination for travelers seeking a blend of nature, history, and culture.</p>
          <h4 style="color: #EFDDB6;">Natural Beauty and Diverse Landscapes</h4>
          <p>Sri Lanka's natural beauty is unparalleled, with its diverse landscapes ranging from golden sandy beaches and verdant tea plantations to dense rainforests and arid plains. The island's coastline is dotted with idyllic beaches that attract sun-seekers, surfers, and divers alike. The southern and eastern coasts, in particular, are famous for their pristine shores and crystal-clear waters. Mirissa, with its tranquil atmosphere and whale-watching tours, and Unawatuna, known for its vibrant nightlife and coral reefs, are among the most popular beach destinations.</p>
          <p>Moving inland, the hill country of Sri Lanka offers a stark contrast to the coastal regions. The central highlands are a picturesque area covered with rolling hills and lush tea plantations. Nuwara Eliya, often referred to as "Little England" due to its cool climate and colonial-era architecture, is a charming town surrounded by tea estates. The breathtaking views from Horton Plains National Park, including the dramatic drop at World's End, are a highlight for nature lovers and hikers.</p>
          <h4 style="color: #EFDDB6;">Rich Cultural Heritage</h4>
          <p>Sri Lanka's cultural heritage is as rich and diverse as its natural beauty. The island is home to several UNESCO World Heritage Sites that reflect its ancient history and religious significance. Anuradhapura, one of the island's ancient capitals, is a sprawling complex of well-preserved ruins that date back to the third century BCE. The city is renowned for its ancient stupas, monasteries, and the sacred Bodhi Tree, under which Buddha is said to have attained enlightenment.
             Another significant cultural site is the rock fortress of Sigiriya, also known as Lion Rock. This ancient citadel, built in the fifth century CE, stands majestically atop a granite peak. The climb to the summit offers panoramic views of the surrounding countryside, and visitors can explore the ruins of the palace, gardens, and frescoes along the way. Nearby, the Dambulla Cave Temple, a complex of five caves adorned with Buddha statues and intricate murals, is a testament to Sri Lanka's Buddhist heritage.</p>
          <p>Kandy, the cultural capital of Sri Lanka, is home to the Temple of the Tooth Relic, one of the most sacred sites for Buddhists. This temple, located in the heart of the city, houses a tooth relic of the Buddha and is the focal point of the annual Esala Perahera festival, a grand procession featuring traditional dancers, drummers, and beautifully decorated elephants.</p>
          <h4 style="color: #EFDDB6;">Wildlife and Natural Reserves</h4>
          <p>Sri Lanka's diverse ecosystems support a wide array of wildlife, making it a haven for nature enthusiasts. The island is home to numerous national parks and wildlife sanctuaries where visitors can experience its rich biodiversity. Yala National Park, located in the southeastern region, is the most visited and second largest national park in Sri Lanka. It is renowned for its high density of leopards, as well as elephants, crocodiles, and a variety of bird species.</p>
          <p>In the central province, Minneriya National Park is famous for the annual gathering of wild elephants. During the dry season, hundreds of elephants congregate around the Minneriya Tank, creating a spectacular sight. Udawalawe National Park, situated in the southern part of the island, is another excellent destination for elephant sightings and bird watching.
              Marine life around Sri Lanka is equally impressive. The waters off the coast of Trincomalee, on the northeastern shore, are ideal for diving and snorkeling, with vibrant coral reefs and a rich array of marine species. Pigeon Island National Park, located just off the coast, is a protected area that offers some of the best snorkeling experiences in the country.</p>
          <h4 style="color: #EFDDB6;">Wildlife and Natural Reserves</h4>          
          <p> Sri Lanka, formerly known as Ceylon, is an island country in South Asia, located in the Indian Ocean. It is known for its diverse landscapes ranging from rainforest and arid plains to highlands and sandy beaches. The country boasts a rich cultural heritage, famous tea plantations, and an array of wildlife. Colombo, the capital, is a vibrant city with colonial buildings and modern amenities.</p>
